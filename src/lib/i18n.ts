export const TRANSLATIONS: Record<Lang, typeof en> = { en, ru, tr }

export function detectLang(): Lang {
  const saved = localStorage.getItem('app_lang') as Lang | null
  if (saved && saved in TRANSLATIONS) return saved

  const tgLang = (window as any).Telegram?.WebApp?.initDataUnsafe?.user?.language_code as string | undefined
  const navLang = (tgLang || navigator.language || '').toLowerCase().slice(0, 2)

  if (navLang === 'ru') return 'ru'
  if (navLang === 'tr') return 'tr'
  return 'en'
}

export function saveLang(lang: Lang) {
  localStorage.setItem('app_lang', lang)
}
